// #region Types ------------------------------------------------------------

/**
 * Stacking behaviour shared by the cartesian chart wrappers.
 *
 * - `default` — each series drawn independently.
 * - `stacked` — series share a `stackId` and pile on top of each other.
 * - `percent` — stacked and normalised to 100% (recharts `stackOffset="expand"`).
 */
type ChartType = "default" | "stacked" | "percent";

/**
 * Orientation of a cartesian/radial chart.
 *
 * - `horizontal` — categories on the X-axis (the common case).
 * - `vertical` — categories on the Y-axis.
 * - `radial` — used by {@link PieChart}-style radial charts.
 */
type ChartLayout = "horizontal" | "vertical" | "radial";

/** Recharts axis tick interval strategy. */
type IntervalType = "preserveStartEnd" | "equidistantPreserveStart";

/** A loosely-typed chart data row. */
type ChartDatum = Record<string, unknown>;

/**
 * Per-series configuration consumed by every chart in the design system.
 *
 * Each key is a data series identifier mapping to a display label, an
 * optional icon component, and a color source. Colors may be one of the
 * built-in {@link CHART_COLORS} keys (e.g. `"chart-1"`), any raw CSS color
 * string, or a `theme` map keyed by light/dark for theme-scoped values
 * generated by {@link ChartStyle}.
 */
type ChartConfig = {
  [k in string]: {
    label?: React.ReactNode;
    icon?: React.ComponentType;
  } & (
    | { color?: ChartColorKeys; theme?: never }
    | { color?: never; theme: Record<keyof typeof THEMES, string> }
  );
};

/**
 * The five themeable chart color tokens, mapped to their CSS custom
 * properties. Use these keys in {@link ChartConfig} `color` fields to stay
 * on-palette in both light and dark modes.
 */
const CHART_COLORS = {
  "chart-1": "var(--chart-1)",
  "chart-2": "var(--chart-2)",
  "chart-3": "var(--chart-3)",
  "chart-4": "var(--chart-4)",
  "chart-5": "var(--chart-5)",
} as const;

type ChartColorKeys = keyof typeof CHART_COLORS | (string & {});

/** Default color cycle applied to series when none is specified. */
const DEFAULT_COLORS = [
  "chart-1",
  "chart-2",
  "chart-3",
  "chart-4",
  "chart-5",
] as const;

type ChartContextProps = {
  config: ChartConfig;
  data?: ChartDatum[];
  layout: ChartLayout;
  dataKey?: string;
  /** The series currently isolated by the interactive legend, or `null`. */
  selectedLegend: string | null;
  /** Sets (or clears, with `null`) the isolated legend series. */
  onLegendSelect: (legendItem: string | null) => void;
};

const ChartContext = createContext<ChartContextProps | null>(null);

/**
 * Reads the nearest {@link Chart} / {@link ChartContainer} context.
 *
 * Used by the layout-aware {@link XAxis}, {@link YAxis},
 * {@link CartesianGrid}, the {@link ChartTooltipContent}, and the
 * interactive {@link ChartLegendContent}.
 */
export function useChart() {
  const context = use(ChartContext);

  if (!context) {
    throw new Error("useChart must be used within a <Chart />");
  }

  return context;
}

// #region Helpers ----------------------------------------------------------

/** Formats a 0–1 ratio as an integer percentage string (e.g. `0.42` → `"42%"`). */
export function valueToPercent(value: number) {
  return `${(value * 100).toFixed(0)}%`;
}

/**
 * Minimal controlled/uncontrolled state hook. When `value` is provided the
 * state is controlled (the setter only forwards to `onChange`); otherwise it
 * falls back to internal state seeded from `defaultValue`. A `value` of `null`
 * still counts as controlled — only `undefined` selects uncontrolled mode.
 */
function useControllableState<T>({
  value,
  defaultValue,
  onChange,
}: {
  value?: T;
  defaultValue: T;
  onChange?: (next: T) => void;
}): [T, (next: T) => void] {
  const isControlled = value !== undefined;
  const [uncontrolled, setUncontrolled] = useState<T>(defaultValue);
  const resolved = value === undefined ? uncontrolled : value;

  const setValue = useCallback(
    (next: T) => {
      if (!isControlled) {
        setUncontrolled(next);
      }
      onChange?.(next);
    },
    [isControlled, onChange]
  );

  return [resolved, setValue];
}

/**
 * Builds a `category → color key` map, cycling through `colors` so charts
 * with more series than colors wrap around the palette.
 */
const constructCategoryColors = (
  categories: string[],
  colors: readonly ChartColorKeys[]
): Map<string, ChartColorKeys> => {
  const categoryColors = new Map<string, ChartColorKeys>();

  let index = 0;
  for (const category of categories) {
    const color = colors[index % colors.length];
    if (color !== undefined) {
      categoryColors.set(category, color);
    }
    index += 1;
  }

  return categoryColors;
};

/**
 * Resolves a color source to a concrete CSS value: built-in token keys map
 * to their `var(--chart-N)`, raw strings pass through, and `undefined`
 * falls back to the first chart color.
 */
const getColorValue = (color?: string): string => {
  if (!color) {
    return "var(--chart-1)";
  }

  return CHART_COLORS[color as keyof typeof CHART_COLORS] ?? color;
};

/**
 * Resolves the {@link ChartConfig} entry for a recharts tooltip/legend
 * payload item, walking both the top-level payload and recharts' nested
 * `payload.payload` data row before falling back to the literal key.
 */
function getPayloadConfigFromPayload(
  config: ChartConfig,
  payload: unknown,
  key: string
) {
  if (typeof payload !== "object" || payload === null) {
    return;
  }

  const payloadPayload =
    "payload" in payload &&
    typeof payload.payload === "object" &&
    payload.payload !== null
      ? payload.payload
      : undefined;

  let configLabelKey: string = key;

  if (
    key in payload &&
    typeof payload[key as keyof typeof payload] === "string"
  ) {
    configLabelKey = payload[key as keyof typeof payload] as string;
  } else if (
    payloadPayload &&
    key in payloadPayload &&
    typeof payloadPayload[key as keyof typeof payloadPayload] === "string"
  ) {
    configLabelKey = payloadPayload[
      key as keyof typeof payloadPayload
    ] as string;
  }

  return configLabelKey in config
    ? config[configLabelKey]
    : config[key as keyof typeof config];
}

