/**
 * Root wrapper for the data-driven chart components.
 *
 * Provides the {@link ChartConfig}, data, layout, and interactive-legend
 * state to descendants via context, injects per-series `--color-<key>` CSS
 * variables through {@link ChartStyle}, and renders a recharts
 * `ResponsiveContainer`. `children` may be a single recharts element or a
 * render-prop receiving the chart context (used by the wrappers to read
 * `selectedLegend` / `onLegendSelect`).
 *
 * @remarks
 * - Prefer the high-level wrappers ({@link AreaChart}, {@link BarChart},
 *   {@link LineChart}, {@link PieChart}); reach for `Chart` directly only
 *   when composing custom recharts series.
 * - For the simpler shadcn-style primitive (config context without the
 *   interactive legend / data requirement) use {@link ChartContainer}.
 *
 * @example
 * ```tsx
 * <Chart config={config} data={data} dataKey="month">
 *   {() => (
 *     <BarChartPrimitive data={data}>
 *       <XAxis />
 *       <YAxis />
 *       <Bar dataKey="revenue" />
 *     </BarChartPrimitive>
 *   )}
 * </Chart>
 * ```
 */
const Chart = ({
  id,
  className,
  children,
  config,
  data,
  dataKey,
  ref,
  layout = "horizontal",
  containerHeight,
  selectedSeries,
  defaultSelectedSeries = null,
  onSelectedSeriesChange,
  ...props
}: Omit<React.ComponentProps<"div">, "children"> &
  Pick<ChartContextProps, "data" | "dataKey"> & {
    config: ChartConfig;
    containerHeight?: number;
    layout?: ChartLayout;
    selectedSeries?: string | null;
    defaultSelectedSeries?: string | null;
    onSelectedSeriesChange?: (value: string | null) => void;
    children: ReactElement | ((props: ChartContextProps) => ReactElement);
  }) => {
  const isMobile = useIsMobile();
  const uniqueId = useId();
  const chartId = useMemo(
    () => `chart-${id || uniqueId.replace(/:/g, "")}`,
    [id, uniqueId]
  );

  const [selectedLegend, onLegendSelect] = useControllableState<string | null>({
    value: selectedSeries,
    defaultValue: defaultSelectedSeries ?? null,
    onChange: onSelectedSeriesChange,
  });

  const value = useMemo<ChartContextProps>(
    () => ({
      config,
      selectedLegend,
      onLegendSelect,
      data: data ?? [],
      dataKey: dataKey ?? "value",
      layout,
    }),
    [config, selectedLegend, onLegendSelect, data, dataKey, layout]
  );

  return (
    <ChartContext value={value}>
      <div
        className={cn(chartRootClassName, className)}
        data-chart={chartId}
        data-slot="chart"
        ref={ref}
        {...props}
      >
        <ChartStyle config={config} id={chartId} />
        <ResponsiveContainer height={containerHeight ?? (isMobile ? 200 : 370)}>
          {typeof children === "function" ? children(value) : children}
        </ResponsiveContainer>
      </div>
    </ChartContext>
  );
};

const noop = () => {
  // intentionally empty — default legend handler for ChartContainer
};

/**
 * Lightweight chart root that only supplies the {@link ChartConfig} context
 * (plus a no-op legend state) and a `ResponsiveContainer`.
 *
 * Use this when you want to compose recharts primitives by hand without the
 * data-driven {@link Chart} wrapper. The {@link ChartTooltipContent} and
 * {@link ChartLegendContent} bodies work inside either root.
 *
 * @example
 * ```tsx
 * <ChartContainer config={config} className="aspect-video">
 *   <BarChartPrimitive data={data}>
 *     <Bar dataKey="revenue" fill="var(--color-revenue)" />
 *   </BarChartPrimitive>
 * </ChartContainer>
 * ```
 */
function ChartContainer({
  id,
  className,
  children,
  config,
  ...props
}: React.ComponentProps<"div"> & {
  config: ChartConfig;
  children: React.ComponentProps<typeof ResponsiveContainer>["children"];
}) {
  const uniqueId = useId();
  const chartId = `chart-${id || uniqueId.replace(/:/g, "")}`;

  const value = useMemo<ChartContextProps>(
    () => ({
      config,
      selectedLegend: null,
      onLegendSelect: noop,
      data: [],
      dataKey: "value",
      layout: "horizontal",
    }),
    [config]
  );

  return (
    <ChartContext value={value}>
      <div
        className={cn(
          "aspect-video [&_.recharts-cartesian-axis-tick_text]:fill-muted-foreground [&_.recharts-cartesian-grid_line[stroke='#ccc']]:stroke-border/50",
          chartRootClassName,
          className
        )}
        data-chart={chartId}
        data-slot="chart"
        {...props}
      >
        <ChartStyle config={config} id={chartId} />
        <ResponsiveContainer>{children}</ResponsiveContainer>
      </div>
    </ChartContext>
  );
}

/**
 * Injects a scoped `<style>` declaring `--color-<key>` custom properties for
 * each series under `[data-chart=<id>]`, for both light and dark themes.
 * Entries without a `color` or `theme` are skipped. Called internally by
 * {@link Chart} and {@link ChartContainer}.
 */
const ChartStyle = ({ id, config }: { id: string; config: ChartConfig }) => {
  const colorConfig = Object.entries(config).filter(
    ([, conf]) => conf.theme || conf.color
  );

  if (!colorConfig.length) {
    return null;
  }

  return (
    <style
      // biome-ignore lint/security/noDangerouslySetInnerHtml: static chart-theme CSS derived from build-time ChartConfig, no user input
      dangerouslySetInnerHTML={{
        __html: Object.entries(THEMES)
          .map(
            ([theme, prefix]) => `
${prefix} [data-chart=${id}] {
${colorConfig
  .map(([key, itemConfig]) => {
    const themed = itemConfig.theme?.[theme as keyof typeof itemConfig.theme];
    const color = getColorValue(themed || itemConfig.color);
    return color ? `  --color-${key}: ${color};` : null;
  })
  .join("\n")}
}
`
          )
          .join("\n"),
      }}
    />
  );
};

