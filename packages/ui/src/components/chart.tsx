/** Props for {@link ChartSeriesSelector}. */
interface ChartSeriesSelectorProps
  extends Omit<React.ComponentProps<"div">, "onChange" | "defaultValue"> {
  /** Clicking the active series clears the selection. Defaults to `true`. */
  allowDeselect?: boolean;
  /** Palette for the color dots; cycles when shorter than the series count. */
  colors?: readonly ChartColorKeys[];
  /** Per-series {@link ChartConfig}; one toggle is rendered per key. */
  config: ChartConfig;
  /** Data rows used to compute each series' total when `showTotal`. */
  data?: ChartDatum[];
  /** Uncontrolled initial selection. Defaults to `null`. */
  defaultValue?: string | null;
  /** Fires with the new selection (or `null` when cleared). */
  onValueChange?: (value: string | null) => void;
  /** Restrict and order the rendered series. Defaults to `Object.keys(config)`. */
  series?: string[];
  /** Render each series' summed total beneath its label (dashboard style). */
  showTotal?: boolean;
  /** Controlled selected series key, or `null` for none. */
  value?: string | null;
  /** Formats the per-series total. Defaults to `toLocaleString`. */
  valueFormatter?: (value: number) => string;
}

const defaultTotalFormatter = (value: number) => value.toLocaleString();

/** Sums a numeric `key` across `data`, ignoring non-numeric cells. */
function sumSeries(data: ChartDatum[] | undefined, key: string): number {
  if (!data) {
    return 0;
  }

  let total = 0;
  for (const row of data) {
    const cell = row[key];
    if (typeof cell === "number") {
      total += cell;
    }
  }

  return total;
}

/**
 * Header-mounted metric selector for the chart family.
 *
 * Renders one toggle per `config` series — a color dot + label, plus an
 * optional summed total — driving a single "selected series" value. Pair it
 * with a chart by sharing state: feed the same `value` to the chart's
 * `selectedSeries`, and update from both this `onValueChange` and the chart's
 * `onSelectedSeriesChange`, so the header toggles and the in-chart interactive
 * legend stay in lockstep (clicking a series isolates it; clicking it again
 * clears).
 *
 * Controlled (`value` + `onValueChange`) or uncontrolled (`defaultValue`).
 *
 * @example
 * ```tsx
 * const [series, setSeries] = useState<string | null>("revenue");
 * return (
 *   <>
 *     <ChartSeriesSelector
 *       config={config}
 *       data={data}
 *       onValueChange={setSeries}
 *       showTotal
 *       value={series}
 *     />
 *     <BarChart
 *       config={config}
 *       data={data}
 *       dataKey="month"
 *       onSelectedSeriesChange={setSeries}
 *       selectedSeries={series}
 *     />
 *   </>
 * );
 * ```
 */
function ChartSeriesSelector({
  config,
  series,
  value,
  defaultValue = null,
  onValueChange,
  colors = DEFAULT_COLORS,
  data,
  valueFormatter = defaultTotalFormatter,
  showTotal = false,
  allowDeselect = true,
  className,
  ...props
}: ChartSeriesSelectorProps) {
  const keys = series ?? Object.keys(config);
  const categoryColors = constructCategoryColors(keys, colors);
  const [selected, setSelected] = useControllableState<string | null>({
    value,
    defaultValue,
    onChange: onValueChange,
  });

  return (
    <div
      className={cn(
        showTotal
          ? "flex flex-wrap items-stretch gap-2"
          : "flex flex-wrap items-center gap-1.5",
        className
      )}
      data-slot="chart-series-selector"
      role="toolbar"
      {...props}
    >
      {keys.map((key) => {
        const itemConfig = config[key];
        const isActive = selected === key;
        const color = getColorValue(
          itemConfig?.color || categoryColors.get(key)
        );
        const label = itemConfig?.label ?? key;
        const handleClick = () =>
          setSelected(isActive && allowDeselect ? null : key);

        if (showTotal) {
          return (
            <button
              aria-pressed={isActive}
              className={cn(
                "flex flex-1 flex-col gap-1 rounded-lg border px-3 py-2 text-left transition-colors hover:bg-secondary/50 focus-visible:outline-none focus-visible:ring-3 focus-visible:ring-ring/50",
                isActive
                  ? "border-foreground/20 bg-secondary/60"
                  : "border-transparent bg-secondary/20"
              )}
              data-active={isActive}
              data-slot="chart-series-selector-item"
              key={key}
              onClick={handleClick}
              type="button"
            >
              <span className="flex items-center gap-1.5 text-muted-foreground text-xs">
                <span
                  aria-hidden
                  className="size-2 shrink-0 rounded-full"
                  style={{ backgroundColor: color }}
                />
                {label}
              </span>
              <span className="font-medium font-mono text-foreground text-lg tabular-nums">
                {valueFormatter(sumSeries(data, key))}
              </span>
            </button>
          );
        }

        return (
          <button
            aria-pressed={isActive}
            className={cn(
              "flex items-center gap-1.5 rounded-sm px-2 py-1 text-muted-foreground text-xs transition-colors hover:bg-secondary/70 hover:text-secondary-foreground focus-visible:outline-none focus-visible:ring-3 focus-visible:ring-ring/50",
              isActive && "bg-secondary/70 text-secondary-foreground"
            )}
            data-active={isActive}
            data-slot="chart-series-selector-item"
            key={key}
            onClick={handleClick}
            type="button"
          >
            <span
              aria-hidden
              className="size-2.5 shrink-0 rounded-full"
              style={{ backgroundColor: color }}
            />
            {label}
          </button>
        );
      })}
    </div>
  );
}

