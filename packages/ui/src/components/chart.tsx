type ChartTooltipContentExtras = {
  showLabel?: boolean;
  labelSeparator?: boolean;
  showIndicator?: boolean;
  indicator?: "line" | "dot" | "dashed";
  nameKey?: string;
  labelKey?: string;
};

/**
 * Frosted-glass tooltip body rendered inside `<ChartTooltip content={…} />`.
 *
 * Reads series labels/colors from the enclosing chart config and supports
 * three indicator styles (`dot`, `line`, `dashed`), an optional label
 * separator, label suppression, and a custom `formatter` override.
 *
 * @remarks
 * - When a single payload entry is present with a non-`dot` indicator the
 *   label is nested inline beside the value.
 * - Pass `nameKey` / `labelKey` to resolve config entries by a field other
 *   than the recharts-inferred one.
 */
const ChartTooltipContent = <TValue extends ValueType, TName extends NameType>({
  payload,
  className,
  indicator = "dot",
  showLabel = true,
  showIndicator = true,
  label,
  labelSeparator = true,
  labelFormatter,
  labelClassName,
  formatter,
  color,
  nameKey,
  labelKey,
  ref,
}: TooltipContentProps<TValue, TName> &
  React.ComponentProps<"div"> &
  ChartTooltipContentExtras) => {
  const { config } = useChart();

  const tooltipLabel = useMemo(() => {
    if (!(showLabel && payload?.length)) {
      return null;
    }

    const [item] = payload;
    if (!item) {
      return null;
    }

    const key = `${labelKey || item.dataKey || item.name || "value"}`;
    const itemConfig = getPayloadConfigFromPayload(config, item, key);
    const value =
      !labelKey && typeof label === "string"
        ? config[label]?.label || label
        : itemConfig?.label;

    if (labelFormatter) {
      return (
        <div className={labelClassName}>{labelFormatter(value, payload)}</div>
      );
    }

    if (!value) {
      return null;
    }

    return <div className={labelClassName}>{value}</div>;
  }, [
    label,
    labelFormatter,
    payload,
    showLabel,
    labelClassName,
    config,
    labelKey,
  ]);

  if (!payload?.length) {
    return null;
  }

  const nestLabel = payload.length === 1 && indicator !== "dot";

  return (
    <div
      className={cn(
        "grid min-w-48 items-start rounded-lg bg-popover/70 p-3 py-2 text-popover-foreground text-xs shadow-md ring-1 ring-foreground/10 backdrop-blur-lg",
        className
      )}
      ref={ref}
    >
      {showLabel ? (
        <>
          {nestLabel ? null : (
            <span className="font-medium">{tooltipLabel}</span>
          )}
          {labelSeparator && (
            <span
              aria-hidden
              className="mt-2 mb-3 block h-px w-full bg-border/70"
            />
          )}
        </>
      ) : null}
      <div className="grid gap-3">
        {payload.map((item, index) => {
          const key = `${nameKey || item.name || item.dataKey || "value"}`;
          const itemConfig = getPayloadConfigFromPayload(config, item, key);
          const indicatorColor = color || item.payload?.fill || item.color;

          return (
            <div
              className={cn(
                "flex w-full flex-wrap items-stretch gap-2 *:[svg]:text-muted-foreground",
                indicator === "dot" && "items-center *:[svg]:size-2.5",
                indicator === "line" && "*:[svg]:h-full *:[svg]:w-2.5"
              )}
              key={key}
            >
              {formatter && item?.value !== undefined && item.name ? (
                formatter(item.value, item.name, item, index, item.payload)
              ) : (
                <>
                  {renderIndicator({
                    itemConfig,
                    hideIndicator: !showIndicator,
                    indicator,
                    nestLabel,
                    indicatorColor,
                  })}
                  <div
                    className={cn(
                      "flex flex-1 justify-between leading-none",
                      nestLabel ? "items-end" : "items-center"
                    )}
                  >
                    <div className="grid gap-1.5">
                      {nestLabel ? tooltipLabel : null}
                      <span className="text-muted-foreground">
                        {itemConfig?.label || item.name}
                      </span>
                    </div>
                    {item.value !== undefined && item.value !== null && (
                      <span className="font-medium font-mono text-foreground tabular-nums">
                        {item.value.toLocaleString()}
                      </span>
                    )}
                  </div>
                </>
              )}
            </div>
          );
        })}
      </div>
    </div>
  );
};

/** Renders the colored dot/line/dashed marker (or config icon) for a tooltip row. */
function renderIndicator({
  itemConfig,
  hideIndicator,
  indicator,
  nestLabel,
  indicatorColor,
}: {
  itemConfig: ReturnType<typeof getPayloadConfigFromPayload>;
  hideIndicator: boolean;
  indicator: "line" | "dot" | "dashed";
  nestLabel: boolean;
  indicatorColor?: string;
}) {
  if (itemConfig?.icon) {
    return <itemConfig.icon />;
  }

  if (hideIndicator) {
    return null;
  }

  return (
    <div
      className={cn(
        "shrink-0 rounded-full border-(--color-border) bg-(--color-bg)",
        indicator === "dot" && "size-2.5",
        indicator === "line" && "w-1",
        indicator === "dashed" &&
          "w-0 border-[1.5px] border-dashed bg-transparent",
        nestLabel && indicator === "dashed" && "my-0.5"
      )}
      style={
        {
          "--color-bg": indicatorColor,
          "--color-border": indicatorColor,
        } as React.CSSProperties
      }
    />
  );
}

// #region Legend content ---------------------------------------------------

/** Props for {@link ChartLegendContent}. */
type ChartLegendContentProps = React.ComponentProps<"div"> &
  Pick<LegendProps, "align" | "verticalAlign"> & {
    /** Recharts legend payload injected automatically by the `Legend` wrapper. */
    payload?: readonly LegendPayload[];
    /** When `true`, shows the color icon/dot for each legend entry. */
    showIcon?: boolean;
    /** Data key used to look up the series label in {@link ChartConfig}. */
    nameKey?: string;
  };

const legendJustify = {
  right: "justify-end",
  left: "justify-start",
  center: "justify-center",
} as const;

/**
 * Interactive legend body rendered inside `<ChartLegend content={…} />`.
 *
 * Each entry is a toggle button: clicking isolates that series (dimming the
 * rest); clicking it again clears the selection. State lives in the chart
 * context (`selectedLegend` / `onLegendSelect`) so the series rendering can
 * react to it.
 */
const ChartLegendContent = ({
  className,
  showIcon = true,
  payload,
  align = "right",
  verticalAlign = "bottom",
  nameKey,
}: ChartLegendContentProps) => {
  const { config, selectedLegend, onLegendSelect } = useChart();

  if (!payload?.length) {
    return null;
  }

  return (
    <div
      className={cn(
        "flex flex-wrap items-center gap-x-1",
        verticalAlign === "top" ? "pb-3" : "pt-3",
        legendJustify[align ?? "right"],
        className
      )}
    >
      {payload.map((item) => {
        const key = `${nameKey || item.dataKey || "value"}`;
        const itemConfig = getPayloadConfigFromPayload(config, item, key);
        const selected = selectedLegend === key;

        return (
          <button
            aria-pressed={selected}
            className={cn(
              "flex items-center gap-2 rounded-sm px-2 py-1 text-muted-foreground *:[svg]:-mx-0.5 *:[svg]:size-2.5 *:[svg]:shrink-0 *:[svg]:text-muted-foreground",
              "hover:bg-secondary/70 hover:text-secondary-foreground focus-visible:outline-none focus-visible:ring-3 focus-visible:ring-ring/50",
              selected && "bg-secondary/70 text-secondary-foreground"
            )}
            key={key}
            onClick={(event) => {
              // Recharts renders the legend inside the chart wrapper, which
              // carries the chart-level onClick that clears the selection.
              // Stop the bubble so the toggle isn't immediately undone.
              event.stopPropagation();
              onLegendSelect(selected ? null : key);
            }}
            type="button"
          >
            {itemConfig?.icon && showIcon ? (
              <itemConfig.icon />
            ) : (
              <div
                className="size-2.5 rounded-full"
                style={{ backgroundColor: item.color }}
              />
            )}
            {itemConfig?.label}
          </button>
        );
      })}
    </div>
  );
};

