const tooltipWrapperStyle = { outline: "none" } as const;

const cursorStyleRadial = {
  stroke: "var(--muted)",
  strokeWidth: 0.1,
  fill: "var(--muted)",
  fillOpacity: 0.5,
} as const;

const cursorStyleDefault = {
  stroke: "var(--muted)",
  strokeWidth: 1,
  fill: "var(--muted)",
  fillOpacity: 0.5,
} as const;

/**
 * Recharts `Tooltip` pre-wired with a token-driven cursor that adapts to the
 * chart layout (radial vs. cartesian). Pair with {@link ChartTooltipContent}.
 */
const ChartTooltip = (props: TooltipProps) => {
  const { layout } = useChart();
  const cursorStyle =
    layout === "radial" ? cursorStyleRadial : cursorStyleDefault;

  return (
    <TooltipPrimitive
      cursor={cursorStyle}
      wrapperStyle={tooltipWrapperStyle}
      {...props}
    />
  );
};

