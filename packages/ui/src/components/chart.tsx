/** Props for {@link XAxis}. */
interface XAxisProps extends Omit<XAxisPrimitiveProps, "ref"> {
  /** Render only the first and last tick labels, hiding all intermediate ones. */
  displayEdgeLabelsOnly?: boolean;
  /** Recharts tick interval strategy forwarded to the `XAxis` primitive. */
  intervalType?: IntervalType;
}

const tickHorizontal = { transform: "translate(32, 6)" } as const;

/**
 * Layout-aware recharts `XAxis`. Reads `dataKey`/`data`/`layout` from the
 * chart context, hides its line/border, and supports edge-only labels.
 */
const XAxis = ({
  displayEdgeLabelsOnly,
  className,
  intervalType = "preserveStartEnd",
  minTickGap = 5,
  type,
  ...props
}: XAxisProps) => {
  const { dataKey, data, layout } = useChart();

  // In a vertical layout the X axis carries the numeric values, so it must be
  // a number axis with no category dataKey. Defaulting to category here (the
  // recharts default) produces NaN ticks because there is no category source.
  const isVertical = layout === "vertical";

  const ticks =
    displayEdgeLabelsOnly && data?.length && dataKey
      ? [data[0]?.[dataKey], data.at(-1)?.[dataKey]]
      : undefined;

  const tick = layout === "horizontal" ? tickHorizontal : undefined;

  return (
    <XAxisPrimitive
      axisLine={false}
      className={cn(
        "text-muted-foreground text-xs **:[text]:fill-muted-foreground",
        className
      )}
      dataKey={layout === "horizontal" ? dataKey : undefined}
      interval={displayEdgeLabelsOnly ? "preserveStartEnd" : intervalType}
      minTickGap={minTickGap}
      tick={tick as XAxisPrimitiveProps["tick"]}
      tickLine={false}
      ticks={ticks as XAxisPrimitiveProps["ticks"]}
      type={type ?? (isVertical ? "number" : "category")}
      {...props}
    />
  );
};

const yAxisTickHorizontal = { transform: "translate(-3, 0)" } as const;
const yAxisTickVertical = { transform: "translate(0, 0)" } as const;

/**
 * Layout-aware recharts `YAxis`. Swaps category/number roles and tick
 * placement based on the chart `layout` from context.
 */
const YAxis = ({
  className,
  width,
  domain = ["auto", "auto"],
  type,
  ...props
}: Omit<YAxisPrimitiveProps, "ref">) => {
  const { layout, dataKey } = useChart();

  const isHorizontal = layout === "horizontal";
  const tick = isHorizontal ? yAxisTickHorizontal : yAxisTickVertical;
  const computedWidth = width ?? (isHorizontal ? 40 : 80);
  const axisType = type ?? (isHorizontal ? "number" : "category");

  return (
    <YAxisPrimitive
      axisLine={false}
      className={cn(
        "text-muted-foreground text-xs **:[text]:fill-muted-foreground",
        className
      )}
      dataKey={isHorizontal ? undefined : dataKey}
      domain={domain}
      interval={isHorizontal ? undefined : "equidistantPreserveStart"}
      tick={tick}
      tickLine={false}
      type={axisType}
      width={computedWidth}
      {...props}
    />
  );
};

/** Layout-aware recharts `CartesianGrid` drawing only the relevant axis lines. */
const CartesianGrid = ({ className, ...props }: CartesianGridProps) => {
  const { layout } = useChart();
  return (
    <CartesianGridPrimitive
      className={cn("stroke-1 stroke-muted", className)}
      horizontal={layout !== "vertical"}
      vertical={layout === "vertical"}
      {...props}
    />
  );
};

